'''
In python variables are in smaller cases
and classes start in big cases
'''

name = "Varun"
print(type(name)) # return output as <class 'str'>

num=3
print(type(num)) # return output as <class 'int'>

nfloat = 3.1222
print(type(nfloat)) # return output as <class 'float'>

ncomplex = 3j
print(type(ncomplex)) # return output as <class 'complex'>


